namespace fstl {

/*****************************************************************************************/
// max
// 返回两个值中的大值
/*****************************************************************************************/
template <class T>
const T& max(const T& lhs, const T& rhs) {
  return lhs > rhs ? lhs : rhs;
}

/*****************************************************************************************/
// min
// 返回两个值中的小值
/*****************************************************************************************/
template <class T>
const T& min(const T& lhs, const T& rhs) {
  return lhs < rhs ? lhs : rhs;
}
};  // namespace fstl